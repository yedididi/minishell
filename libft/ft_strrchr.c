char	*ft_strrchr(const char *s, int c)
{
	char	*ptr_saved;

	ptr_saved = 0;
	while (1)
	{
		if (*s == (char)c)
			ptr_saved = (char *)s;
		if (*s == '\0')
			return (ptr_saved);
		s++;
	}
}
